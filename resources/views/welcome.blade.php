    <h4>Cierre de la frontera</h4>
    <p class="text-justify">La frontera entre Estados Unidos y México fue cerrada el 18 de marzo para todos los viajes
        no esenciales para prevenir y evitar la propagación del nuevo
        coronavirus. La medida suspendió entre otros, el programa de asilo, dejando a la deriva en territorio mexicano a
        miles de personas que aguardan sus audiencias
        judiciales y cerrando la puerta a nuevos peticionarios, tal el caso de menores de edad.

        La medida, que en un comienzo fue por 30 días, fue extendida en abril por un período similar de tiempo. Pero el
        martes el Departamento de Seguridad
        Nacional (DHS) anunció que el cierre de la frontera con México para viajes no esenciales será “indefinido”,
        afectando principalmente el asilo y el
        denominado Programa de Protección del Migrante, que desde que fue activado en enero del año pasado ha devuelto a
        México a más de 65,000
        personas, entre hombres, mujeres y niños.</p>

