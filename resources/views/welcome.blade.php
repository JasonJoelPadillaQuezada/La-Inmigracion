    <h4>Inmigrantes llegan con Covid-19</h4>
    <p class="text-justify">Un total de 68 menores inmigrantes bajo custodia de las autoridades de los Estados Unidos,
        han dado positivo del COVID-19, tras haber
        entrado al país sin acompañante adulto, informó el lunes la Oficina estadounidense de Reasentamiento de
        Refugiados.
        El cable de EFE señala que la ORR indicó en un comunicado, que hasta el pasado 7 de mayo ha contabilizado 68
        casos de menores con COVID-19, según han
        determinado las pruebas de laboratorio, de los que 43 se encontraban en Illinois, 11 en Nueva York, 8 en Texas,
        y 6 en Pensilvania.
        Sin embargo, defensores de derechos humanos denunciaron en redes sociales que del total de menores con COVID-19,
        al menos unos 10 serían hondureños.
        Del total de contagiados, según dio a conocer EFE, 63 se han recuperado y ya están fuera de la cuarentena.
        Además, catorce de ellos ya han sido reunificados
        con un tutor, que pueden ser sus padres, un representante legal, un pariente adulto, una persona elegida por sus
        progenitores o una organización afiliada a la ORR.
        Actualmente, un total de cinco menores no acompañados han dado positivo del COVID-19 y están en aislamiento
        médico», puntualiza la nota, que agrega que ninguno ha requerido de hospitalización.
    </p>

