                    <h4>Origen regional y nacional</h4>
                    <p class="text-justify">
                        Orden regional y nacionalLa distribución regional de los migrantes internacionales está liderada por Europa que alberga a 82 millones de personas, seguidas por América del Norte 
                        con 59 millones, mientras que África del Norte y Asia Occidental hospedan a 49 millones.
                        Sin embargo, los datos recopilados demuestran que, pese a que Europa y Norteamérica alojan al mayor número de migrantes, la población que se desplazó al norte de África, el Asia
                        occidental y el África subsahariana creció más rápidamente.
                        Del mismo modo, y contrariamente a la opinión popular, la mayoría de los migrantes internacionales se desplazan entre países situados dentro de la misma región.
                        Así un 80% de los migrantes internacionales en el África subsahariana, el 83% de Asia oriental y sudoriental, el 73% de América Latina y el Caribe (73%) y el 63% de Asia central
                        y meridional  proceden de la región en la que residen.
                        En cambio, la mayoría de los que vivían en América del Norte, un 98%, Oceanía un 88%, y África del Norte y Asia Occidental, un59% nacieron fuera de su región de residencia.
                        El país que recibió a más migrantes fue Estados Unidos con 51 millones, seguidos por Alemania y Arabia Saudita con unos 13 millones respectivamente, Rusia con 12 millones, 
                        el Reino Unido con 10 millones y los Emiratos Árabes Unidos con 9 millones.
                    </p>

                    <br>

                    <h4>Distribución por países de origen, edad y sexo</h4>
                    <p class="text-justify">
                        En cuanto a su lugar de nacimiento, un tercio de todos los migrantes internacionales proceden de sólo diez países. El país líder en este apartado es India con 18 millones 
                        de personas, seguidos a una distancia considerable por los 12 millones de mexicanos que viven en el extranjero, junto a 11 millones de chinos y 10 millones de rusos.
                        En términos de edad, uno de cada siete migrantes internacionales es menor de 20 años. Durante este año, el conjunto de datos indica que 38 millones de migrantes 
                        internacionales, una cifra equivalente al 14% de la población migrante mundial, tenían menos de 20 años. La mayor proporción de jóvenes de todos los migrantes internacionales 
                        la acogió el África subsahariana, en torno a un 27%, seguida de América Latina y el Caribe, y África septentrional y Asia occidental, alrededor del 22% cada una.
                        Además, tres de cada cuatro migrantes internacionales, o unos 202 millones, están en edad laboral, fijada entre los 20 y los 64 años.
                        En la distribución por sexos, las mujeres representan algo menos de la mitad de todos los migrantes internacionales en 2019 y disminuyó ligeramente, del 49% en 2000 
                        al 48% en 2019. La mayor proporción de mujeres migrantes se registró en América del Norte (52%) y Europa (51%), y la más baja en el África subsahariana (47%) y en el África
                        septentrional y Asia occidental (36%).
