                    <h4>Crece el número de desplazamientos forzados</h4>
                    <p class="text-justify">
                       Otro apunte destacado del estudio es que los desplazamientos forzados a través de las fronteras internacionales siguen aumentando.
                       Entre los años 2010 y 2017, el número mundial de refugiados y solicitantes de asilo aumentó en unos 13 millones, lo que representa cerca de la cuarta parte del aumento 
                       del número total de migrantes internacionales.  África septentrional y Asia occidental acogieron alrededor del 46% del número mundial de refugiados y solicitantes de asilo, 
                       seguidos por el África subsahariana (21%).
                       Las cifras del estudio del Departamento de Asuntos Económicos y sociales de las Naciones Unidas se basan en estadísticas oficiales nacionales a partir de censos de población, 
                       registros de población o encuestas representativas a nivel nacional.
