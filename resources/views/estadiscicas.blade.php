                    <p class="text-justify"> 
                       Un informe de la ONU que evalúa las últimas tendencias en migración internacional señala el aumento de ese número de personas durante
                        la última década, el liderazgo de Europa y América del Norte como regiones con un mayor número de receptores y que la mayoría de ellos
                        se desplazan entre países situados dentro de la misma región.
                        El número de migrantes internacionales a nivel global ascendió en la actualidad a 272 millones, un registro que indica un incremento de 51 millones de personas desde el año 2010.
                        Pese a que el número de migrantes internacionales en todo el mundo representan una proporción bastante pequeña respecto a la población total, esta ha aumentado del 2,8% en 2000 al
                        3,5% en 2019, lo que significa que en los últimos años el número de migrantes internacionales ha crecido más rápidamente que la población mundial.
                        Todas estas nuevas estimaciones forman parte del estudio de población de migrantes internacionales 2019, un conjunto de datos compilado por el Departamento de Asuntos Económicos y
                        sociales de las Naciones Unidas (DESA), donde se publican las estimaciones de ese conjunto de población por edad, sexo y origen para todos los países y zonas del mundo.
                        El secretario general adjunto de DESA, Liu Zhenmin, destacó la relevancia de contar con estos datos para comprender el importante papel de los migrantes y la migración en el desarrollo
                         tanto de los países de origen como de destino.
