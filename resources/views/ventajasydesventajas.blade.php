                     <h3>Aspectos positivos y negativos en la inmigración:</h3>
                    <p class="text-justify"> 
                        Si hay un debate que ahora mismo está abierto y todo el mundo tiene opinión sobre él es el de la inmigración. Es un hecho que son muchos los factores que atraen a 
                        personas de otros países a Europa y, concretamente, a España, en busca de una vida mejor, de otras oportunidades o simplemente porque quieren continuar su carrera aquí.
                        No podemos obviar los tremendos conflictos armados que en países como Siria están generando millones de refugiados en todo el mundo. Personas como nosotros, que vivían
                        una vida normal y tranquila y que se encuentran de pronto con que han de huir de su casa, dejarla vacía, llevarse a su familia y protegerla en cualquier otro lugar.
                        Por eso mismo muchísimas ciudades y países tienen diferentes políticas de acogida para los inmigrantes especialmente los refugiados.
                    <br>
                    <br>
                        <h4>Ventajas</h4>
                        <h5>1.- Mejora de la calidad de vida</h5>
                         <p> La mayoría de los inmigrantes buscan un aumento en las condiciones de vida estándares. Vivir en un entorno libre de delincuencia,
                            con beneficios ciudadanos y seguridad social, suele ser un motor común para instar al movimiento de la población de un país a otro.
                         </p>
                
                    <h5>2.- Nuevas experiencias personales y profesionales</h5>
                     <p> El nuevo escenario de vida exhorta a los inmigrantes a sacar lo mejor de ellos en el ámbito personal y laboral.
                        Al salir de su zona de confort, los inmigrantes se ven en la necesidad de adoptar nuevos retos: ser más independientes, vivir solos, ahorrar,
                        asumir nuevos compromisos personales y profesionales. Todo en el marco del crecimiento y de la acumulación de nuevas experiencias.
                     </p>

                     <h5>3.- Contacto con nuevas culturas y expansión del conocimiento</h5>
                     <p> Viajar da la oportunidad de conocer nuevas culturas, gastronomías, medios de transporte, modismos, tradiciones, e incluso superar barreras 
                         idiomáticas, si es el caso.
                         Hacer vida en otro país abre las puertas del conocimiento a través de la historia autóctona de cada nación, e incluso mediante recorridos 
                         de los sitios museos o sitios históricos más representativos del lugar.
                     </p>

                         <br>

                     <h4>Desventajas</h4>
                     <h5>1.- Xenofobia y barreras culturales</h5>
                      <p> No todos los países son hospitalarios con los inmigrantes. En muchas ocasiones, prevalecen culturas de resistencia al cambio, en las 
                        cuales los inmigrantes pueden ser víctimas de discriminación racial o de comentarios xenófobos, por su país o región de origen.
                      </p>
                     
                      <h5>2.- Barreras Idiomáticas</h5>
                      <p> Si se opta por migrar hacia un país con un idioma distinto a la lengua materna, resulta elemental conocer al menos las nociones básicas de dicho idioma.
                          Para ello, lo más recomendable es conocer los términos elementales para desenvolverse en sociedad: frases básicas para realizar las compras, estar 
                          familiarizados con los letreros de tránsito y, por supuesto, manejar a cabalidad los saludos y expresiones de cortesía.
                      </p>

                      <h5>3.- Escasez de oferta laboral</h5>
                      <p> El desempleo puede representar una desventaja significativa para el inmigrante, sobre todo si se decide vivir en otro país sin validar las credenciales 
                          académicas y/o profesionales que se tengan en el país de origen.
                          Definitivamente, al hacer vida en otro país, y vivir la experiencia de la multiculturalidad, se pone en práctica la educación, los preceptos culturales 
                          y los valores aprendidos en casa.
                      </p>


